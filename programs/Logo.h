class Logo:public Program {
  public:
    int option = -1;
    String text = "ACDGV";
	int offsetY = 0;
	int space = 4;
	int started = 0;
	int pause2s = 0;
	int direction = -1;
	int startedTime;

    void init() {
		this->startedTime = millis();
	}

    void tick() {
      Surface & s2x1 = surfaces[0];
      Surface & s8x1 = surfaces[1];

	  if (s2x1.facingUp) {
		OLED & screen = channels[s2x1.channel].ports[1].screen;
		screen.clear();
		for (int i=0; i<5; i++) {
			screen.ssd1306.drawBitmap(this->offsetY + (32 + this->space) * i, 0, logo_ACDGV_32x32[i], 32, 32, SSD1306_WHITE);
		}
		screen.needsRefresh = true;
		if (
			(millis() - this->started) > 2000 && 
			(millis() - this->pause2s) > 2000
		) {
			this->offsetY += this->direction;
			if (this->offsetY < -50) {
				this->direction = -this->direction;
				this->pause2s = millis();
			}
			if (this->offsetY > 0) {
				this->direction = -this->direction;
				this->pause2s = millis();
			}
		}
	  }
	  
      if (s8x1.facingUp) {
        s8x1.clear();
		this->option = channels[0].ports[5].re.position;
		int xStep = this->option > 1 ? this->option : 8;
		int yStep = this->option > 1 ? this->option : 8;
		for (int p=0; p<8; p++) {
			for (int x=0; x<128; x+=xStep) {
				for (int y=0; y<32; y+=yStep) {
					channels[s8x1.channel].ports[p].screen.ssd1306.drawLine(x-1,y-1,x+128-1,y-1,SSD1306_WHITE);
					channels[s8x1.channel].ports[p].screen.ssd1306.drawLine(x-1,y-1,x-1,y+32-1,SSD1306_WHITE);
					channels[s8x1.channel].ports[p].screen.needsRefresh = true;
				}
			}
		}
		for (int i=0; i<5; i++) {
			channels[1].ports[i+1].screen.ssd1306.drawBitmap(32,0,logo_ACDGV_64x32[i],64,32,SSD1306_WHITE);
        	channels[1].ports[i+1].screen.needsRefresh = true;
		}
      }
    }

    void setOption(int option) {
      this->option = option;
    }

	void becameActive() {
		this->offsetY = 0;
		this->started = millis();
	}

  Logo() {}
};
